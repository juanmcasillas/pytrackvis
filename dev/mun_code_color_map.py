import random
import os

class ColorGenerator:
    def __init__(self):
        self.used_colors = []


    def gen_color(self):
        while True:
            r = random.randrange(0,256)
            g = random.randrange(0,256)
            b = random.randrange(0,256)
            col = "#%02X%02x%02x".upper() % (r,g,b)
            if not col in self.used_colors:
                self.used_colors.append(col)
                return col

    def gen_dict(self, data, name):
        v = []
        for i in data:
            color = self.gen_color()
            v.append( "\t'%s': '%s'," % (i, color))
        
        s = "%s = {\n%s\n}" % (name, os.linesep.join(v))
        return s
    
